void solve()
{
    int n, k;
    cin >> n >> k;

    int arr[n];
    for (int i = 0; i < n; i++)
        cin >> arr[i];

    sort(arr, arr + n);

    int ans = 1;
    int cnt = 1;
    for (int i = 1; i < n; i++)
    {
        if (arr[i] - arr[i - 1] > k)
            cnt = 1;
        else
            cnt++;

        ans = max(ans, cnt);
    }

    cout << n - ans;
}

int32_t main()
{
    fastio;

    int t;
    cin >> t;

    while (t--)
    {
        solve();
        cout << '\n';
    }

    return 0;
}
