void solve()
{
    int n;
    cin >> n;

    int lsb = __builtin_ffs(n);

    int ans = 1 << (lsb - 1);
    if ((ans ^ n) == 0)
    {
        if (n == 1)
            cout << ans + 2;
        else
            cout << ans + 1;

        return;
    }

    cout << ans;
}

