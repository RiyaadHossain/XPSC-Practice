void solve()
{
    int n, m;
    cin >> n >> m;

    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int cnt1 = count(all(a), -1);
    int cnt2 = count(all(a), -2);

    set<int> st(all(a));
    st.erase(-1);
    st.erase(-2);

    int it = 1;
    int ans = max(cnt1 + sz(st), cnt2 + sz(st));
    for (int s : st)
    {
        int tmp = 1;
        int demand1 = it - 1, demand2 = sz(st) - it;
        tmp += min(s - 1, demand1 + cnt1);
        tmp += min(m - s, demand2 + cnt2);
        ans = max(ans, tmp);
        it++;
    }

    ans = min(m, ans);
    print(ans);
}

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;

    while (t--)
    {
        solve();
        cout << '\n';
    }

    return 0;
}