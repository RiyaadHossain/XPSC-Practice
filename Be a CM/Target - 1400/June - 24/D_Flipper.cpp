void solve()
{
    int n;
    cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int idx = 1;
    for (int i = 1; i < n; i++)
        if (a[i] > a[idx])
            idx = i;

    vi ans;
    for (int i = idx; i < n; i++)
        ans.pb(a[i]);

    if (idx == n - 1)
        idx--;
    else
    {
        ans.pb(a[idx - 1]);
        idx -= 2;
    }

    for (; idx > 0 && a[idx] > a[0]; idx--)
        ans.pb(a[idx]);

    for (int i = 0; i <= idx; i++)
        ans.pb(a[i]);

    for (int i : ans)
        printsp(i);
}

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;

    while (t--)
    {
        solve();
        cout << '\n';
    }

    return 0;
}