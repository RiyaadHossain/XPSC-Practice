int powermod(int x, int y, int p)
{
    int res = 1;
    x = x % p;
    if (x == 0)
        return 0;
    while (y > 0)
    {
        if (y & 1)
            res = (res * x) % p;
        y = y >> 1;
        x = (x * x) % p;
    }
    return res;
}

int modInverse(int n, int p = mod)
{
    return powermod(n, p - 2, p);
}

int nCr(int n, int r, int p = mod)
{
    if (n < r)
        return 0;

    if (r == 0)
        return 1;

    vector<int> fac(n + 1, 0);
    fac[0] = 1;
    for (int i = 1; i <= n; i++)
        fac[i] = (fac[i - 1] * i) % p;

    return (fac[n] * modInverse(fac[r], p) % p * modInverse(fac[n - r], p) % p) % p;
}

void solve()
{
    int n;
    cin >> n;

    vi a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int ans = 1;
    for (int i = 0; i < n; i += 3)
    {
        vi vec = {a[i], a[i + 1], a[i + 2]};
        sort(rall(vec));

        if (vec[0] == vec[2])
            ans *= 3;
        else if (vec[1] == vec[2])
            ans *= 2;
        ans %= mod;
    }

    ans *= nCr(n / 3, n / 6);
    ans %= mod;

    print(ans);
}

