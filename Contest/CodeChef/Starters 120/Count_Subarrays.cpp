void solve()
{
    int n;
    cin >> n;

    int arr[n];
    for (int i = 0; i < n; i++)
        cin >> arr[i];

    int sum;
    vector<int> ans(n + 1, 0);
    for (int i = 0; i < n; i++)
    {
        sum = 0;
        for (int j = i; j < n; j++)
        {
            sum += arr[j];
            if (sum <= n)
                ans[sum]++;
            else
                break;
        }
    }

    for (int i = 1; i <= n; i++)
        cout << ans[i] << " ";
}

