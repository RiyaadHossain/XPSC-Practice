void solve()
{
    int n;
    cin >> n;

    int arr[n];
    for (int i = 0; i < n; i++)
        cin >> arr[i];

    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        if (arr[i] == 1)
        {
            ans += n;
            continue;
        }

        for (int j = 0; j < min(30ll, n); j++)
            ans += (pow(arr[i], j + 1) <= arr[j]);
    }

    cout << ans;
}

